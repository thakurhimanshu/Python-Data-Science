# DIGITAL CALCULATOR
n1=float(input("ENTER THE VALUE"))
n2=float(input("ENTER THE VALUE"))
add=n1+n2
sub=n1-n2
mul=n1*n2
div=n1/n2
floor=n1//n2
exp=n1**n2
mod=n1%n2

print(f'{n1} + {n2} = {add}')
print(f'{n1} - {n2} = {sub}')
print(f'{n1} * {n2} = {mul}')
print(f'{n1} / {n2} = {div}')
print(f'{n1} // {n2} = {floor}')
print(f'{n1} ** {n2} = {exp}')
print(f'{n1} % {n2} = {mod}')